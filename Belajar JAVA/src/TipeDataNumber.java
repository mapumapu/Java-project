public class TipeDataNumber {

    public static void main(String[] args) {

        // all with maximum value//

        // you can add underscore (_) make it easier to read //
        // this is Integer//
        byte iniByte = 127; // size 1 byte //
        short iniShort = 32_767; // size 2 byte //
        int iniInt = 2_147_483_647; // size 4 byte //
        long iniLong = 999_999_999; // size 8 byte //
        long iniLong2 = 10_000_000L; // for data type long you kan add "L" to identify data type long //

        // this is float//
        float iniFloat = 10.10F; // size 4 byte // // for data type float you kan add "F" to identify data type float //
        double iniDouble = 12.121212; // size 8 byte //

        // literals //
        int decimalInt = 25;
        int hexInteger = 0xA132B;
        int binaryInteger = 0b01010101;

    }
}

